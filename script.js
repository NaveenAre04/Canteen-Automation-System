function add1() {

    var value = parseInt(document.getElementById('result1').value, 10);
    value = isNaN(value) ? 0 : value;
    value++;
    document.getElementById('result1').value = value;
    
}
function sub1() {

    var value = parseInt(document.getElementById('result1').value, 10);
    value = isNaN(value) ? 0 : value;
    value--;
    document.getElementById('result1').value = value;
    
}
